int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb == 0 || nb == 1)
		return (0);
	if (nb == 2)
		return (1);
	while (i <= (nb / 2) + 1)
	{
		if (nb % i == 0 && nb / 1 == nb && nb / nb == 1)
			return (0);
		i++;
	}
	return (1);
}
